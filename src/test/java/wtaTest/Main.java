    public static class ArrayListSetAll<T> extends ArrayList<T>{
        private Supplier<T> newT;

        @Override
        public T set(int index, T element) {
            sizeTo(index);
            return super.set(index, element);
        }

        @Override
        public T get(int index) {
            sizeTo(index);
            return super.get(index);
        }

        public void sizeTo(int index){
            while (index>=size()){
                add(null);
            }
        }
    }

    public static class ArrayListSetAllNorm<T> extends ArrayListSetAll<T>{
        private Supplier<T> newT;

        public  ArrayListSetAllNorm(Supplier<T> addDefault){
            newT=addDefault;
        }

        @Override
        public void sizeTo(int index) {
            while (index>=size()){
                add(newT.get());
            }
        }
    }

    public static String nbtToList(String nbt){
        nbt=nbt
                .replaceAll(" ", "")
                .replaceAll("\\t", "")
                .replaceAll("\\n", "");
        ArrayListSetAllNorm<ArrayList<List<Integer>>> lists=new ArrayListSetAllNorm<>(ArrayList::new);
        while (nbt!=""){
            int pos2=nbt.indexOf('}');
            if (pos2==-1){
                break;
            }
            String block=nbt.substring(0, pos2);
            int state=Integer.parseInt(block.substring(block.indexOf("state:")).replaceAll("[^0-9]", ""));
            String valueS=block.substring(0, block.indexOf(']')).replaceAll("[^0-9,]", "");
            String[] valueS2=valueS.split(",");
            ArrayList<Integer> value=new ArrayList<>();
            for (String stringI : valueS2){
                if (stringI==""){
                    continue;
                }
                value.add(Integer.parseInt(stringI));
            }
            lists.get(state).add(value);
            nbt=nbt.substring(pos2+1);
        }

        String ret="";
        int size=lists.size();
        for (int i=0; i<size; i++) {
            ret+="int[][] state"+i+" = "+lists.get(i).toString().replaceAll("\\[", "{").replaceAll("]", "}")+";\n";
        }
        return ret;
    }
    
